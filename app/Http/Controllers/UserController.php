    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);
        $user->firstName = $request->Auth::user()->firstName;
        $user->lasttName = $request->Auth::user()->lasttName;
        $user->phone = $request->Auth::user()->phone;
        $user->email = $request->Auth::user()->email;
        if($request->hasFile('avatar')){
            $fill = $request->file('avatar');
            $extension = $fill->getClientOriginalExtension();
            $fullFill = time().'.'.$extension;
            $fill->move('uploads/avatars/',$fullFill);
        }
        $user->avatar = $fullFill; 
    }

